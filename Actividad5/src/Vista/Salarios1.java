    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (this.txtNombre.getText().length() == 0 || this.txtEdad.getText().length()==0 || this.txtNumTrabajador.getText().length()==0 || this.txtPuesto.getText().length()==0 || this.txtSueldo.getText().length()==0){
        javax.swing.JOptionPane.showMessageDialog(null,"Favor de llenar todos los campos","Error",JOptionPane.ERROR_MESSAGE);
        txtNumTrabajador.requestFocus();
        }else{
        DefaultTableModel _modelo = (DefaultTableModel) tblSalarios.getModel();
        _modelo.addRow(new Object[]{this.txtNumTrabajador.getText(),this.txtNombre.getText(),this.txtEdad.getText(),this.sexo,this.txtPuesto.getText(),this.cmbTipo.getSelectedItem(),this.txtSueldo.getText()});
        
        tblSalarios.setModel(_modelo);
        txtNombre.setText(null);
        txtNumTrabajador.setText(null);
        txtPuesto.setText(null);
        txtSueldo.setText(null);
        txtEdad.setText(null);
        txtNumTrabajador.setFocusable(true);
        txtNumTrabajador.requestFocus();
        }    
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel _modelo = (DefaultTableModel) tblSalarios.getModel();
        int seleccion = tblSalarios.getSelectedRow();
        if (seleccion>=0){
        Object[]opciones = {"Si","No"};    
        int confirmacion = JOptionPane.showOptionDialog(null, "Â¿Esta seguro?", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,opciones,"f");
        if (confirmacion==0){
        _modelo.removeRow(seleccion);
        tblSalarios.setModel(_modelo);
           
        }else{
            javax.swing.JOptionPane.showMessageDialog(null,"No se ha eliminado nada","Info",JOptionPane.INFORMATION_MESSAGE);
        }
        }else{
           javax.swing.JOptionPane.showMessageDialog(null,"No hay nada que elimniar","Error",JOptionPane.ERROR_MESSAGE); 
        }
        txtNumTrabajador.requestFocus();
    }//GEN-LAST:event_btnEliminarActionPerformed
    
